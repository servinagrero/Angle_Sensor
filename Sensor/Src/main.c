// Inicializa los perifericos a la config basica
void user_init(void)
{
	EXTI->IMR |= GPIO_PIN_13; // Unmask EXTI13
	HAL_NVIC_EnableIRQ(EXTI15_10_IRQn);  //Enable EXTI13 in NVIC

	// Timer en PWM para mover el servo
	TIM2->PSC=10;
	TIM2->ARR=0x63999;
	TIM2->CCR1=3799;
	TIM2->CNT=0;

	HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);

	// Timer usado para leer el sensor
	TIM3->PSC=64;
	TIM3->ARR=0x34999;

	HAL_TIM_IC_Stop_IT(&htim3,TIM_CHANNEL_1);
	HAL_TIM_Base_Stop_IT(&htim3);

	// Habilitamos poder recibir datos del uart para cambiar de modo
	HAL_UART_Receive_IT(&huart2, &uart_recieve, sizeof(uint8_t));

	// ADC para leer la posicion del potenciometro
	__HAL_ADC_ENABLE(&hadc);
	HAL_ADC_Start(&hadc);
	HAL_ADC_Start_IT(&hadc);
}


// Muestra por el puerto serie los datos
// Si alguna distancia es mayor de 250cm o menor de 2cm
// se muestra que la distancia esta fuera de rango
void print_data(uint16_t *distances, int num_data) {

  for(int d = 0; d < num_data; ++d) {
      if(distances[d] > 400 || distances[d] < 2) {
	  printf("M[%d] is out of range.\n", d);
      } else {
	  printf("M[%d] (%d) at %u cm.\n", d, angles[d], distances[d]);
      }
  }
  printf("\n");
}

// Habilitamos o deshabilitamos los perifericos en funcion del modo al que vamos a pasar
//
// Modo automatico:
//   Servo automatico con un timer (TIM2)
//   Captura de datos con un timer (TIM3)
//
// Modo manual:
//   Servo manual con el adc (ADC)
//   Movimiento manual del servo (TIM2)
//   Captura de datos con el boton (EXTI)
void gestionar_modo(uint16_t modo) {

    if(modo == MODO_A) {

	__HAL_ADC_DISABLE(&hadc);
	HAL_ADC_Stop_IT(&hadc);

	HAL_TIM_Base_Start(&htim9);
	HAL_TIM_Base_Start_IT(&htim9);

	TIM2->CCR1 = D_M90;


    } else if (modo == MODO_M) {

	HAL_TIM_IC_Stop_IT(&htim3,TIM_CHANNEL_1);
	HAL_TIM_Base_Stop_IT(&htim3);


	HAL_TIM_Base_Stop(&htim9);
	HAL_TIM_Base_Stop_IT(&htim9);

	__HAL_ADC_ENABLE(&hadc);
	HAL_ADC_Start_IT(&hadc);
    }
}

// Recibimos input del usuario
// Permite cambiar entre modos de funcionamiento
// TODO: Add mas modos?,
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {

  __disable_irq();

  if (huart->Instance == USART2) {
      if(strcmp(uart_recieve,"c") || strcmp(uart_recieve, "C"))
	  if (modo == MODO_M) modo = MODO_A;
	  else modo = MODO_M;
	  gestionar_modo(modo);
	  HAL_UART_Receive_IT(&huart2, &uart_recieve, sizeof(uint8_t));
      }
  __enable_irq();
}

